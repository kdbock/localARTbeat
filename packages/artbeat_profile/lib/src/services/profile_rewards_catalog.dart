String profileBadgeCategory(String badgeId) {
  if (badgeId.contains('quest') ||
      badgeId.contains('weekly') ||
      badgeId.contains('streak') ||
      badgeId.contains('perfect')) {
    return 'Quest';
  }
  if (badgeId.contains('walk') ||
      badgeId.contains('explorer') ||
      badgeId.contains('guide')) {
    return 'Explorer';
  }
  if (badgeId.contains('social') ||
      badgeId.contains('helpful') ||
      badgeId.contains('community')) {
    return 'Social';
  }
  if (badgeId.contains('capture') ||
      badgeId.contains('creator') ||
      badgeId.contains('review') ||
      badgeId.contains('contributor')) {
    return 'Creator';
  }
  if (badgeId.contains('level') ||
      badgeId.contains('enthusiast') ||
      badgeId.contains('influencer')) {
    return 'Level';
  }
  return 'Quest';
}
